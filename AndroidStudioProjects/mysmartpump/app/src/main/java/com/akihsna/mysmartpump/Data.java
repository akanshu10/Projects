package com.akihsna.mysmartpump;

public class Data {
    String data;
    String id;
    int output;
    public void Data(){

    }

    public int getOutput() {
        return output;
    }

    public String getData() {
        return data;
    }

    public String getId() {
        return id;
    }

    public Data(String id, String data, int output) {
        this.data = data;
        this.id = this.id;
        this.output = output;
    }
}
